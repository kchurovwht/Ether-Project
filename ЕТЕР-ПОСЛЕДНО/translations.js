// ── Language switcher engine ──
const LANG_KEY = 'stillnes_lang';

function getLang() {
  return localStorage.getItem(LANG_KEY) || 'bg';
}

function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  applyLang(lang);
  updateLangBtn(lang);
  updatePageTitle(lang);
}

function updateLangBtn(lang) {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  // Update html lang attribute
  document.documentElement.lang = lang;
}

function t(key) {
  const lang = getLang();
  return (T[lang] && T[lang][key]) || (T['bg'] && T['bg'][key]) || key;
}

function applyLang(lang) {
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.dataset.t;
    if (T[lang] && T[lang][key] !== undefined) {
      el.textContent = T[lang][key];
    }
  });
  document.querySelectorAll('[data-t-href]').forEach(el => {
    const key = el.dataset.tHref;
    if (T[lang] && T[lang][key] !== undefined) {
      const base = el.dataset.tHrefBase || '';
      el.href = base + encodeURIComponent(T[lang][key]);
    }
  });
  // Re-render schedule if present
  if (typeof renderSchedule === 'function') renderSchedule();
  if (typeof renderServiceSchedule === 'function') renderServiceSchedule();
}

function initLang() {
  const lang = getLang();
  applyLang(lang);
  updateLangBtn(lang);
  updatePageTitle(lang);
}

function updatePageTitle(lang) {
  const el = document.querySelector('[data-t-title]');
  if (!el) return;
  const key = el.getAttribute('data-t-title');
  if (T[lang] && T[lang][key]) {
    document.title = T[lang][key] + ' | Stillnes';
  }
}
